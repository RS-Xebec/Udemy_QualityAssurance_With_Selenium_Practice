#Working on Substring

data = 'This is testing World'

#Fetch specific character by using index
print(data[3])
print(data[8])

#Fetch string by giving starting and end index
print(data[2:9])       
 #In python the last value of range will always be ignored   like here 9th index value will be ignored for this range

#Fetch substring by giving starting index only
print(data[2:])


#Fetch substring by giving end index only
print(data[:9])
